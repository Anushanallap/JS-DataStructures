/*Write a Program to show Sum of three Integer adds to ZERO*/


function sum_three(nums)
{
  return nums[0] + nums[1] + nums[2];
}
/*adding numbers to the array list to give 0 as a output*/

console.log(sum_three([1, 0, -1]));  
console.log(sum_three([25, -75, 50])); 
console.log(sum_three([100, -150, 50]));